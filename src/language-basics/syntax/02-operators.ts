// Common operators in TypeScript

// Arithmetic operators
let sum = 5 + 3;
let difference = 10 - 4;
let product = 6 * 3;
let quotient = 15 / 3;
let remainder = 17 % 5;

// Comparison operators
let isEqual = 5 === 5;
let a = 5;
let b = 3;
let isNotEqual = a !== b;
let isGreater = 10 > 5;
let isLess = 5 < 10;

// Logical operators
let andOperator = true && false;
let orOperator = true || false;
let notOperator = !true;

// Assignment operators
let value = 10;
value += 5; // value = value + 5
value -= 3; // value = value - 3
value *= 2; // value = value * 2

// Nullish coalescing
let nullValue: string | null = null;
let result = nullValue ?? "default";
